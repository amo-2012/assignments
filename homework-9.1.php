function multi ( $a, $b )
{
 
    return $a * $b;  

}

        
assert (multi (2,4)  == 8);





function div ( $a, $b )
{
 
    return $a / $b;  

}

        
assert (div (8,4)  == 2);
assert (div (12,2)  == 6);
assert (div (250,125)  == 2);


